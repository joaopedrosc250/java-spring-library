package com.example.demo;

public class Author {
    private int auid;
    private String name;

    public int getAuid() {
        return this.auid;
    }

    public void setAuid(int auid) {
        this.auid = auid;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
